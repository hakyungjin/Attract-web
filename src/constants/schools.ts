/**
 * 학교 검색 함수
 * @param query 검색어
 * @param limit 최대 결과 수
 */
export const searchSchools = (query: string, limit: number = 20): string[] => {
  if (!query.trim()) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  return KOREA_UNIVERSITIES
    .filter(school => school.toLowerCase().includes(normalizedQuery))
    .slice(0, limit);
};

/**
 * 학교 목록 (가나다순 정렬)
 */
export const SORTED_UNIVERSITIES = [...KOREA_UNIVERSITIES].sort((a, b) => 
  a.localeCompare(b, 'ko')
);

